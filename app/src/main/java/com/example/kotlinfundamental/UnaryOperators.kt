package com.example.kotlinfundamental

fun main(args: Array<String>) {

    var num1=10
    var isCheck:Boolean=true

    println("+number ="+ +num1)
    println("-number ="+ -num1)
    println("++number ="+ ++num1)
    println("--number ="+ --num1)
    println("!isChecked ="+ !isCheck)
    println("........................")

    var x=5
    println("result++  ="+ x++)
    println("result  ="+ x)
    println("++result  ="+ ++x)
    println("result  ="+ x)

}